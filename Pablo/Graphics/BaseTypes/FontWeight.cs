    /// </summary>
    public enum FontWeight
    {

        /// <summary>
        /// Specifies a "Normal" font weight.
        /// </summary>
        Normal,

        /// <summary>
        /// Specifies a "Thin" font weight.
        /// </summary>
        Thin,

        /// <summary>
        /// Specifies a "Light" font weight.
        /// </summary>
        Light,

        /// <summary>
        /// Specifies a "Medium" font weight.
        /// </summary>
        Medium,

        /// <summary>
        /// Specifies a "Bold" font weight.
        /// </summary>
        Bold,

        /// <summary>
        /// Specifies a "Black" font weight.
        /// </summary>
        Black

    }
}
